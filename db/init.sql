ON CONFLICT (tipo) DO NOTHING;

CREATE TABLE IF NOT EXISTS movimientos_usuarios ( --tabla que indica los movimientos de cada usuario
    id_movimiento_usuario BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario    BIGINT NOT NULL REFERENCES usuarios(id_usuario) ON DELETE CASCADE,
    id_movimiento BIGINT NOT NULL REFERENCES movimientos(id_movimiento) ON DELETE CASCADE,
    descripcion   VARCHAR(100) NOT NULL,
    fecha_movimiento     DATE,
    fecha_fin_movimiento DATE
);